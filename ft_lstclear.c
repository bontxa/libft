#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*p;

	while (*lst)
	{
		p = *lst;
		(*del)((*lst)->content);
		(*lst) = (*lst)->next;
		free(p);
	}
	p = NULL;
}
